package cricket;

public class Bowler {
    String name;
    float overs;
    int runsGiven;
    int wickets;

    public Bowler(String name, float overs, int runsGiven, int wickets) {
        this.name = name;
        this.overs = overs;
        this.runsGiven = runsGiven;
        this.wickets = wickets;
    }
}
